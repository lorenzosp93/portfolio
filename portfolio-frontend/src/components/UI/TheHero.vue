<script>
export default {
  name: 'TheHero',
  data () {
    return {
      data: [],
      isLoading: false,
      error: null,
      imageLoaded: false,
      aboutLoaded: false,
    }
  },
  watch: {
    isLoading (newValue, oldValue) {
      if (oldValue) {
        this.aboutLoaded = true;
        if (this.imageLoaded) {
          this.$emit('heroLoaded');
        }
      }
    }
  },
  props: {
    observer: Object,
    elementsInView: Array,
  },
  emits: [
    'heroLoaded',
  ],
  inject: [
    'loadData'
  ],
  computed: {
    isVisible () {
      const e = this.elementsInView.filter(elem => elem.target.id == 'the-hero');
      if (e.length == 0) {return true}
      return !e.filter(elem => elem.intersectionRatio > 0)?.length == 0
    }
  },
  methods:{
    loadSettings () {
      let url = '/api/site/settings/';
      this.loadData(url, this);
    },
    imageIsLoaded () {
      this.imageLoaded = true;
      if (this.aboutLoaded) {
        this.$emit('heroLoaded');
      }
    }
  },
  mounted () {
    this.loadSettings()
    this.observer.observe(this.$el);
  }
}
</script>

